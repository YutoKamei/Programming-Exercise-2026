/* 除算における注意点 */
#include<stdio.h>

int main(){
  /* 整数同士の割り算 */
  printf("5 / 2 = %d\n", 5 / 2);
  
  /* 実数同士の割り算 */
  printf("12.4 / 1.2 = %f\n", 12.4 / 1.2);
  
  /* 実数を整数で割る */
  printf("12.4 / 2 = %f\n", 12.4 / 2);
  
  return 0;
}
